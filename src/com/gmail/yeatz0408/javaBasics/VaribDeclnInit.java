package com.gmail.yeatz0408.javaBasics;

public class VaribDeclnInit {

	public static void main(String[] args) {

		int myNumber; //declaration
		
		myNumber = 10; // initialization
		
		boolean myBoolean1; boolean myBoolean2;
		
		// one line
		double myDouble = 7.50;
		
		float myFloat1, myFloat2, myFloat3;
		
		double myDouble1, myDouble2 = 0.3;
		
		int myInt1 = 1, myInt2 = 2, myInt3 = 3;
		
		// int i3; i4;
		
		
		// Only $ and _ can be used in variable names (it also can be used wherever)
		
		int ____anywhere____;
		int $$$$$$$$;
		int $_$_$_$_$_$_;
		
		// illegal examples
		
//		int 3point;
//		int my@street;
		
		
		
	}

}
