package com.gmail.yeatz0408.methods;

public class MethodDesign {
	
	// mandatory: a return type, a method name, a pair of parameter brackets and a pair of body brackets
	// optional: an access modifier, a final && / || static keyword, parameter type(s) + parameter name(s), a body
	
	public void jump() {}
	
	void jump1() {}
	
	// final can come anywhere except after return type
	
	public final void jump2() {}
	
	public static final void jump3() {}
	
	public final static void jump4() {}
	
	final public void jump5() {}
	
	

}
